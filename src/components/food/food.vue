<template>
  <transition name="move">
    <div class="food" v-show="showFlag" ref="food">
      <div class="food-content">
        <div class="image-header">
          <img :src="food.image">
          <div class="back" @click="hide">
            <i class="icon-arrow_lift"></i>
          </div>
        </div>
        <div class="content">
          <h1 class="title">{{food.name}}</h1>
          <div class="detail">
            <span class="sell-count">月售{{food.sellCount}}份</span>
            <span class="rating">好评率{{food.rating}}</span>
          </div>
          <div class="price">
            <span class="now">￥{{food.price}}</span>
            <span class="old" v-show="food.oldPrice">￥{{food.oldPrice}}</span>
          </div>
          <div class="cartcontrol-wrapper">
            <cartcontrol :food="food"></cartcontrol>
          </div>
          <transition name="fade">
            <div @click.stop.prevent="addFirst" class="buy" v-show="!food.count || food.count===0">
              加入购物车
            </div>
          </transition>
        </div>
        <split v-show="food.info"></split>
        <div class="info">
          <h1 class="title">商品信息</h1>
          <p class="text">{{food.info}}</p>
        </div>
        <split></split>
        <div class="rating">
          <h1 class="title">商品评价</h1>
          <!-- 评价选择器 -->
          <ratingselect @select="selectRating" @toggle="toggleContent" :ratings="food.ratings" :selectType="selectType" :onlyContent="onlyContent" :desc="desc"></ratingselect>
          <!-- 评价列表 -->
          <div class="rating-wrapper">
            <!-- 有评价时 -->
            <ul v-show="food.ratings && food.ratings.length">
              <!-- 评价的显示要依据评价的type和是否有内容来显示 -->
              <li v-show="needShow(rating.rateType,rating.text)"  v-for="(rating,index) in food.ratings" :key="index" class="rating-item border-1px">
                <div class="user">
                  <span class="name">{{rating.username}}</span>
                  <img :src="rating.avatar" width="12" height="12" class="avatar">
                </div>
                <div class="time">{{rating.rateTime | formatDate}}</div>
                <div class="text">
                  <span :class="{'icon-thumb_up':rating.rateType===0,'icon-thumb_down':rating.rateType===1}"></span>{{rating.text}}
                </div>
              </li>
            </ul>
            <!-- 暂无评价 -->
            <div class="no-rating" v-show="!food.ratings || !food.ratings.length">暂无评价</div>
          </div>
        </div>
      </div>
    </div>
  </transition>
</template>

<script type="text/ecmascript-6">
  import BScroll from 'better-scroll';
  import Vue from 'vue';
  import cartcontrol from 'components/cartcontrol/cartcontrol';
  import split from 'components/split/split';
  import ratingselect from 'components/ratingselect/ratingselect';
  import {formatDate} from 'common/js/date';

  const ALL = 2;

  export default {
    components: {
      cartcontrol,
      split,
      ratingselect
    },
    props: {
      food: {
        type: Object
      }
    },
    data() {
      return {
        showFlag: false,
        selectType: ALL, // 默认selectType是全部评价
        onlyContent: true, // 默认 只看有内容的评价
        desc: { // 数据dec 为 desc.all, desc.positive, desc.negative
          all: '全部',
          positive: '推荐',
          negative: '吐槽'
        }
      };
    },
    methods: {
      show() {
        this.showFlag = true; // 默认页面展开
        this.selectType = ALL; // 默认 全部评价
        this.onlyContent = true; // 是否只看有内容的评价 默认只看
        this.$nextTick(() => {
          if (!this.scroll) {
            this.scroll = new BScroll(this.$refs.food, {
              click: true
            });
          } else {
            this.scroll.refresh();
          }
        });
      },
      hide() {
        this.showFlag = false;
      },
      // 加入购物车的点击
      addFirst(event) {
        if (!event._constructed) { // 防止浏览器里面出现双重点击
          return;
        }
        // this.$emit('add', event.target); // 触发小球事件
        Vue.set(this.food, 'count', 1); // vue设置food.count =1
      },
      // 全部/满意/吐槽 切换时改变type
      selectRating(type) {
        this.selectType = type;
        this.$nextTick(() => {
          this.scroll.refresh();
        });
      },
      // 根据内容, 判断是否需要显示
      needShow(type, text) { // 通过传递评价的类型和内容
        if (this.onlyContent && !text) { // 如果只看有内容而且当前内容为空 ,那么全部/吐槽/满意都不显示空评价
          return false; // 该评价不显示
        }
        if (this.selectType === ALL) { // 如果显示全部评价
          return true; // 该评价显示
        } else {
          return type === this.selectType; // 如果是满意/吐槽 评价将type返回,则列表只显示type为满意/吐槽的评价
        }
      },
      // 只看有内容的评价 check勾选
      toggleContent() {
        this.onlyContent = !this.onlyContent;
        this.$nextTick(() => {
          this.scroll.refresh();
        });
      }
    },
    filters: {
      formatDate(time) {
        let date = new Date(time);
        return formatDate(date, 'yyyy-MM-dd hh:mm');
      }
    }
};
</script>

